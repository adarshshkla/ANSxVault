def verify_on_chain(tx_hash: str) -> bool:
    """
    Verify a transaction hash exists on the deployed contract.
    Imports CONTRACT_INSTANCE from ui_dashboard at runtime to avoid circular deps.
    """
    try:
        import sys
        sys.path.insert(0, str(Path(__file__).parent.parent / "ui"))
        import ui_dashboard as dash
        if dash.CONTRACT_INSTANCE and dash.W3_INSTANCE:
            # Check the receipt exists on-chain
            receipt = dash.W3_INSTANCE.eth.get_transaction_receipt(tx_hash)
            return receipt is not None and receipt.status == 1
    except Exception as exc:
        print(f"[WATCHDOG] verify_on_chain: {exc}")
    return False


# ────────────────────────────────────────────────────────────
#  FULL PIPELINE
# ────────────────────────────────────────────────────────────

def run_watchdog(tx: dict = None, aes_key: str = None) -> dict:
    """Full watchdog pipeline: analyse → save → upload → return result dict."""
    print("[WATCHDOG] 🚨 Suspicious transaction detected — running pipeline…")

    report   = generate_suspicious_report(tx)
    print(f"[WATCHDOG] Severity: {report['severity']} | Flags: {report['flags']}")

    filepath = save_report_to_file(report, aes_key=aes_key)
    cid      = upload_to_pinata(filepath)

    result = {
        "report":      report,
        "filepath":    str(filepath),
        "cid":         cid,
        "gateway_url": f"{PINATA_GATEWAY}{cid}" if cid else None,
    }
    print("[WATCHDOG] Pipeline complete.")
    return result


